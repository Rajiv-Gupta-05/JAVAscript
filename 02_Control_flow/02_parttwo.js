// Example of switch statement with comments

// Define a variable representing a day of the week
let dayOfWeek = "Monday";

// Check the value of the variable using a switch statement
switch (dayOfWeek) {
    case "Monday":
        console.log("Today is Monday. Time to start the week!");
        break; // Exit the switch statement after executing this block
    case "Tuesday":
        console.log("Today is Tuesday. Have a productive day!");
        break; // Exit the switch statement after executing this block
    case "Wednesday":
        console.log("Today is Wednesday. Halfway through the week!");
        break; // Exit the switch statement after executing this block
    case "Thursday":
        console.log("Today is Thursday. Almost there!");
        break; // Exit the switch statement after executing this block
    case "Friday":
        console.log("Today is Friday. Weekend is coming!");
        break; // Exit the switch statement after executing this block
    case "Saturday":
        console.log("Today is Saturday. Enjoy your weekend!");
        break; // Exit the switch statement after executing this block
    case "Sunday":
        console.log("Today is Sunday. Relax and recharge!");
        break; // Exit the switch statement after executing this block
    default:
        console.log("Invalid day of the week."); // Execute this block if no case matches
}
